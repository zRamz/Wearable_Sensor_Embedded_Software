//        reg_int_cb(int_param);
//#endif
//
//#ifdef AK89xx_SECONDARY
//    setup_compass();
//    if (mpu_set_compass_sample_rate(10))
//        return -1;
//#else
//    /* Already disabled by setup_compass. */
//    if (mpu_set_bypass(0))
